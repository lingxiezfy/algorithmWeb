package vo;

public class Type {
  private int t_id;
  private Source source;
  private String t_name;

  public int getT_id() {
    return t_id;
  }

  public void setT_id(int t_id) {
    this.t_id = t_id;
  }

  public Source getSource() {
    return source;
  }

  public void setSource(Source source) {
    this.source = source;
  }

  public String getT_name() {
    return t_name;
  }

  public void setT_name(String t_name) {
    this.t_name = t_name;
  }
}
